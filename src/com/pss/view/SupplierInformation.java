    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        StringUtile su = new StringUtile();
        IUser user=new UserIMPL();
        if(!su.validateNull(this.txtsname.getText())){
            JOptionPane.showMessageDialog(this, "供应商名称不允许为空！");
            return;
        }if(!su.validateDigit(this.txtuid.getText())){
            JOptionPane.showMessageDialog(this, "请填入正确的负责人编号！");
            return;
        }
         if(!user.IsIDExist(Integer.parseInt(this.txtuid.getText()))){
                JOptionPane.showMessageDialog(this, "负责人编号不存在！");
                return;
            }
        if(!su.validateNull(this.txtgoods.getText())){
            JOptionPane.showMessageDialog(this, "商品名称不允许为空！");
            return;
        }
        if(!su.validateDigit(this.txtprice.getText())){
            JOptionPane.showMessageDialog(this, "商品价格必须为数字！");
            return;
        }
        if(!su.validateNull(this.txtstele.getText())){
            JOptionPane.showMessageDialog(this, "电话不能为空！");
            return;
        }
        if(!su.validateNull(this.txtfax.getText())){
            JOptionPane.showMessageDialog(this, "传真不能为空！");
            return;
        }
        if(!su.validateNull(this.txtfirmaddress.getText())){
            JOptionPane.showMessageDialog(this, "公司地址不能为空！");
            return;
        }
        if(!su.validateNull(this.txtplantaddress.getText())){
            JOptionPane.showMessageDialog(this, "工厂地址不能为空！");
            return;
        }
        Supplier p = new Supplier();
        p.setsName(this.txtsname.getText().trim());//trim()去除前后空格
        p.setuId(Integer.valueOf(this.txtuid.getText().trim()));
        p.setGoods(this.txtgoods.getText().trim());
        p.setPrice(Double.valueOf(this.txtprice.getText().trim()));
        p.setsTele(this.txtstele.getText().trim());
        p.setFax(this.txtfax.getText().trim());
        p.setFirmAddress(this.txtfirmaddress.getText().trim());
        p.setPlantAddress(this.txtplantaddress.getText().trim());
        supplierdao pl = new supplierImpl();
        
        if(pl.IsExist(p.getsName())){
            if(pl.UpdateSupplier(p)>0){
                JOptionPane.showMessageDialog(this, "同名同类商品已存在，该商品库存已更新！");
                btnSearchActionPerformed(evt);
            } else{
                JOptionPane.showMessageDialog(this, "同名同类商品已存在，该商品库存更新失败！");
            }
            btnCancelActionPerformed(evt);
            return;           
        }
        int result = pl.AddSupplier(p);
        if(result > 0){
            JOptionPane.showMessageDialog(this, "添加成功");
            btnCancelActionPerformed(evt);
            btnSearchActionPerformed(evt);
            
        } else {
            JOptionPane.showMessageDialog(this, "添加失败");
            btnCancelActionPerformed(evt);
        }
    }//GEN-LAST:event_btnAddActionPerformed

