    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        StringUtile su = new StringUtile();
        if(!su.validateNull(this.txtsname.getText())){
            supplierdao pl = new supplierImpl();
            List<Supplier> list = pl.QuerySupplier();  
            if(!list.isEmpty()){
                this.btnUpdate.setEnabled(true);
                this.btnDelete.setEnabled(true);
            }
            DefaultTableModel tm = (DefaultTableModel)suppliertable.getModel();
            while (tm.getRowCount() > 0) {
                tm.removeRow(tm.getRowCount() - 1);//逐行清除表中内容
            }

            for(Supplier p:list){
                String [] str = {String.valueOf(p.getsId()), p.getsName(),String.valueOf(p.getuId()),p.getGoods(),Double.toString(p.getPrice()),p.getsTele(),p.getFax(),p.getFirmAddress(),p.getPlantAddress()};
                tm.addRow(str);
            }
        }else{
            supplierdao pl = new supplierImpl();
            String value=this.txtsname.getText();
            List<Supplier> list = pl.QuerySupplierByName(value);  
            if(!list.isEmpty()){
                this.btnUpdate.setEnabled(true);
                this.btnDelete.setEnabled(true);               
            }
            DefaultTableModel tm = (DefaultTableModel)suppliertable.getModel();
            while (tm.getRowCount() > 0) {
                tm.removeRow(tm.getRowCount() - 1);//逐行清除表中内容
            }
            for(Supplier p:list){
                String [] str = {String.valueOf(p.getsId()), p.getsName(),String.valueOf(p.getuId()),p.getGoods(),Double.toString(p.getPrice()),p.getsTele(),p.getFax(),p.getFirmAddress(),p.getPlantAddress()};
                tm.addRow(str);
             }
            if(list.isEmpty()){
                JOptionPane.showMessageDialog(this, "没有该供应商信息");
                this.txtsname.setText("");
            }
        } 
    }//GEN-LAST:event_btnSearchActionPerformed

