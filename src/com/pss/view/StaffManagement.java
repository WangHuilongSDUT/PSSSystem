    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String name = this.txtName.getText().trim();//.trim()去除前后空格 防止空格的输入
        String position = this.txtPosition.getText().trim();
        String phone = this.txtPhone.getText().trim();
        String address = this.txtAddress.getText().trim();
        String sex = this.txtSex.getText().trim();
        String place = this.txtPlace.getText().trim();
        String email = this.txtEmail.getText().trim();
        //判断输入的数据是否为空和价格是否为数字  公用工具类
//            if (sql.equals(0)){
        System.out.println("用户登陆权限：" + LoginFrame.user.getAuthority().equals("0"));
        System.out.println("用户xinxi：" + LoginFrame.user.getuName());
        if (LoginFrame.user.getAuthority().equals("0")) {
            if (StringUtile.validateNull(name) == false) {
                JOptionPane.showMessageDialog(this, "销售员名称不能为空");
                return;
            }
            if (StringUtile.validateNull(position) == false) {
                JOptionPane.showMessageDialog(this, "销售员职位不能为空");
                return;
            }
            if (StringUtile.validateNull(address) == false) {
                JOptionPane.showMessageDialog(this, "销售员现住址不能为空");
                return;
            }
            if (StringUtile.validateNull(position) == false) {
                JOptionPane.showMessageDialog(this, "销售员职位不能为空");
                return;
            }
            if (StringUtile.validateNull(sex) == false) {
                JOptionPane.showMessageDialog(this, "销售员性别不能为空");
                return;
            }
            if (StringUtile.validateNull(place) == false) {
                JOptionPane.showMessageDialog(this, "销售员籍贯不能为空");
                return;
            }
            if (StringUtile.validateNull(email) == false) {
                JOptionPane.showMessageDialog(this, "销售员邮箱不能为空");
                return;
            }
            if (StringUtile.validateDigit(phone) == false) {
                JOptionPane.showMessageDialog(this, "销售员手机号必须为数字");
                return;
            }

            com.pss.po.User p = new com.pss.po.User(name, position, phone, address, sex, place, email);
            if (ipd.isExit(p.getuTele(), p.getEmail())) {

                JOptionPane.showMessageDialog(this, "该电话或邮箱已存在，请重新输入！");
                btnSeachActionPerformed(evt);
                btnConcelActionPerformed(evt);
                return;

            } else {
                int result = ipd.addUser(p);
                if (result > 0) {
                    //在窗口提示添加成功
                    JOptionPane.showMessageDialog(this, "添加成功");
                    btnSeachActionPerformed(evt);
                    btnConcelActionPerformed(evt);
                } else {
                    JOptionPane.showMessageDialog(this, "添加失败");
                    btnConcelActionPerformed(evt);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "请用管理员身份登录");
            btnConcelActionPerformed(evt);
        }
           
 
    }//GEN-LAST:event_btnAddActionPerformed

