    private void btnSeachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeachActionPerformed
        // TODO add your handling code here:
        String str = this.txtSeach.getText();
        List<com.pss.po.User> lp = ipd.getUser(str);
        DefaultTableModel dtm = (DefaultTableModel) this.tblUser.getModel();
        while(dtm.getRowCount()>0){
            dtm.removeRow(0);
        }
        for(com.pss.po.User p :lp){
            Vector v = new Vector();
            v.add(p.getuId());
            v.add(p.getuName());
            v.add(p.getPosition());
            v.add(p.getuTele());
            v.add(p.getuAddress());
            v.add(p.getSex());
            v.add(p.getNativePlace());
            v.add(p.getEmail());
            dtm.addRow(v);
        
        }
    }//GEN-LAST:event_btnSeachActionPerformed

