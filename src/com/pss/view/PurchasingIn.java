    private void tblProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductMouseClicked
        //商品表
        DefaultTableModel tm = (DefaultTableModel) tblProduct.getModel();
        //获取当前行
        int row = this.tblProduct.getSelectedRow();
        String goods = tm.getValueAt(row, 0).toString().trim();
        supplierdao sDao = new supplierImpl();
        List<Supplier> list = sDao.QuerySupplierByGoods(goods);

        if (list != null) {//设置采购按钮是否禁用
            btnAdd.setEnabled(true);
        } else {
            btnAdd.setEnabled(false);
        }
        DefaultTableModel tms = (DefaultTableModel) tblSupplier.getModel();
        while (tms.getRowCount() > 0) {
            tms.removeRow(tm.getRowCount() - 1);//逐行清除表中内容
        }
        
        //最奇怪的bug
        for (Supplier s : list) {
            String[] str = {String.valueOf(s.getsId()), s.getsName(), s.getGoods(), String.valueOf(s.getPrice()), ""};
            tms.addRow(str);
        }

    }//GEN-LAST:event_tblProductMouseClicked

