    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // 采购
        
       // List<String[]> list = new ArrayList<>();
        DefaultTableModel tm = (DefaultTableModel) tblSupplier.getModel();        


        String buyNumber=tblSupplier.getValueAt(tblSupplier.getSelectedRow(),4).toString().trim();
        if (!StringUtile.validateNull(buyNumber)) {
            JOptionPane.showMessageDialog(this, "购买数量不允许为空！");
            return;
        }
        if (!StringUtile.validateDigit(tblSupplier.getValueAt(tblSupplier.getSelectedRow(),4).toString().trim())) {
            JOptionPane.showMessageDialog(this, "购买数量必须为数字！");
            return;
        }   
        JOptionPane.showMessageDialog(this, "购买数量："+buyNumber);
        Buy b = new Buy();
        b.setBuyNo(UUIDUtils.getUUID());//采购单号
        b.setsId(Integer.valueOf(tblSupplier.getValueAt(tblSupplier.getSelectedRow(),0).toString().trim()));//供应商编号
        ProductDao pDao = new ProductImpl();
        for (Product p : pDao.QueryProductByName(String.valueOf(tblSupplier.getValueAt(tblSupplier.getSelectedRow(),2).toString().trim()))) {
            b.setpNo(p.getpNo());//商品编号
        }
        b.setPrice(Double.valueOf(tblSupplier.getValueAt(tblSupplier.getSelectedRow(),3).toString().trim()));//商品价格
        b.setBuyCount(Integer.valueOf(tblSupplier.getValueAt(tblSupplier.getSelectedRow(),4).toString().trim()));//商品数量
        BuyDao bDao = new BuyImpl();
        if (bDao.AddBuy(b) > 0) {
            pDao.UpdateProductStock(b.getpNo(), b.getBuyCount());
            JOptionPane.showMessageDialog(this, "采购成功！");
            //btnSearchActionPerformed(evt);
        } else {
            JOptionPane.showMessageDialog(this, "采购失败！");
        }


    }//GEN-LAST:event_btnAddActionPerformed

